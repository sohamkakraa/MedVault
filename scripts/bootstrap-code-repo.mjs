#!/usr/bin/env node
/**
 * bootstrap-code-repo.mjs
 *
 * One-time generator for CODE_REPO.md. Walks the source tree and produces a
 * per-file registry that future Claudes and developers can read before opening
 * source files.
 *
 * Run once to bootstrap: node scripts/bootstrap-code-repo.mjs
 * Do NOT run on every commit — the registry is maintained manually after bootstrap.
 *
 * Output: CODE_REPO.md at the project root.
 */

import { readFileSync, readdirSync, statSync, writeFileSync } from "fs";
import { join, relative, extname } from "path";

const ROOT = new URL("..", import.meta.url).pathname.replace(/\/$/, "");

const IGNORE_DIRS = new Set([
  "node_modules", ".next", ".git", "dist", "coverage",
  ".claude", "ios-app", "mobile", "murphy",
  "tests/e2e/__screenshots__",
]);
const INCLUDE_EXTS = new Set([".ts", ".tsx", ".mjs", ".prisma"]);

// Simple regex-based export extraction
function extractExports(content) {
  const matches = [];
  const re = /^export\s+(?:default\s+)?(?:async\s+)?(?:function|class|const|type|interface|enum)\s+(\w+)/gm;
  let m;
  while ((m = re.exec(content)) !== null) {
    if (m[1] && !matches.includes(m[1])) matches.push(m[1]);
  }
  // Also pick up `export { ... }`
  const namedRe = /^export\s+\{([^}]+)\}/gm;
  while ((m = namedRe.exec(content)) !== null) {
    m[1].split(",").map((s) => s.trim().split(/\s+as\s+/)[0].trim()).filter(Boolean).forEach((e) => {
      if (!matches.includes(e)) matches.push(e);
    });
  }
  return matches.slice(0, 8); // cap at 8 to keep rows readable
}

// Count lines in file
function countLines(content) {
  return content.split("\n").length;
}

// Walk directory tree
function walk(dir, files = []) {
  for (const entry of readdirSync(dir)) {
    const fullPath = join(dir, entry);
    const rel = relative(ROOT, fullPath);
    if (IGNORE_DIRS.has(entry) || [...IGNORE_DIRS].some((d) => rel.startsWith(d))) continue;
    try {
      const stat = statSync(fullPath);
      if (stat.isDirectory()) {
        walk(fullPath, files);
      } else if (INCLUDE_EXTS.has(extname(entry))) {
        files.push(fullPath);
      }
    } catch {
      // skip unreadable
    }
  }
  return files;
}

// Group files by top-level directory segment under src/
function groupByDir(files) {
  const groups = {};
  for (const f of files) {
    const rel = relative(ROOT, f);
    const parts = rel.split("/");
    const key = parts.slice(0, parts.length > 2 ? 2 : 1).join("/");
    (groups[key] = groups[key] ?? []).push(f);
  }
  return groups;
}

function buildRow(file) {
  const rel = relative(ROOT, file);
  let content = "";
  try { content = readFileSync(file, "utf8"); } catch { return null; }
  const lines = countLines(content);
  const exports = extractExports(content);
  const exportStr = exports.length ? `exports: \`${exports.join("`, `")}\`` : "no named exports";
  return `- \`${rel}\` · ${lines} lines · TODO: describe · ${exportStr}`;
}

const allFiles = walk(ROOT);
const groups = groupByDir(allFiles);

const sections = [];

// Header
sections.push(`# UMA — Code Repo Registry

> **READ THIS FIRST.** Before opening source files, read this index. Use \`Glob\` / \`Grep\` only when this file points you to the right area but doesn't tell you enough. If you make a code change, you must update the matching row in this file in the same commit.

## How to read this file
- **Folder header** = what lives there at a high level.
- **File rows** = filename · line count · one-line purpose · key exports · dependents.
- When in doubt, the source wins; correct this file in the same PR.

> Generated by \`node scripts/bootstrap-code-repo.mjs\` on ${new Date().toISOString().slice(0, 10)}. Update rows manually after initial bootstrap.
`);

for (const [group, files] of Object.entries(groups).sort()) {
  sections.push(`## \`${group}/\``);
  const rows = files
    .sort()
    .map(buildRow)
    .filter(Boolean);
  sections.push(rows.join("\n"));
  sections.push("");
}

const output = sections.join("\n");
writeFileSync(join(ROOT, "CODE_REPO.md"), output, "utf8");
console.log(`\n✓ CODE_REPO.md written (${allFiles.length} files indexed)\n`);
console.log("Next steps:");
console.log("  1. Replace each 'TODO: describe' with a one-line purpose.");
console.log("  2. Add 'used by:' entries for key files.");
console.log("  3. Commit CODE_REPO.md alongside the source changes.\n");
